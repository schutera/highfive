#pragma once

// Embedded TLS trust anchors for the HiveHive ESP32-CAM firmware.
//
// Two anchors are pinned, one per host the firmware talks to:
//
//   * `kIsrgRootX1Pem` — Internet Security Research Group Root X1.
//     Anchor for `highfive.schutera.com` (Let's Encrypt R13
//     intermediate as of 2026-05). Used by `initNewModuleOnServer`
//     in `esp_init.cpp`; `postImage` and `sendHeartbeat` in
//     `client.cpp`; and the manifest + binary fetches in
//     `ota.cpp`'s `httpOtaCheckAndApply`.
//
//   * `kGoogleApisCaBundlePem` — Google Trust Services Root R1 **and**
//     R4 concatenated into one PEM bundle (flash-resident, so it costs
//     no heap; `setCACert` keeps the cert in `.rodata`). This is the
//     only Google anchor consumed by a `setCACert` call, by
//     `attemptGeolocation` in [`esp_init.cpp`]. `www.googleapis.com`
//     serves an R4 (ECC) chain today but historically served R1 (RSA),
//     so trusting both survives a rotation in either direction. The
//     bundle is exactly `kGtsRootR1Pem` followed by `kGtsRootR4Pem`
//     (the documented per-root constants below); `test_native_tls_roots`
//     asserts that equality so the duplicated bytes can never silently
//     diverge.
//
// Every embedded cert is sourced verbatim from the issuing CA's
// published PEM (https://letsencrypt.org/certs/isrgrootx1.pem,
// https://pki.goog/repo/certs/gtsr1.pem, https://pki.goog/repo/certs/gtsr4.pem),
// verified against their well-known SHA-256 fingerprints. Validity
// windows are well beyond the firmware's plausible deployment lifetime,
// but ADR-008 OTA gives a path to rotate without reflashing.
//
// PROGMEM is NOT used — on ESP32 the `.rodata` segment is already
// memory-mapped from flash, so a `constexpr char[]` at namespace
// scope lives in flash automatically. The pointer the CA arrays
// expose has program lifetime, which is the contract
// `WiFiClientSecure::setCACert` requires (the cert pointer must
// outlive every connection that uses it).
//
// Design context: ADR-010 — see
// [docs/09-architecture-decisions/adr-010-esp-firmware-tls-trust-model.md].

